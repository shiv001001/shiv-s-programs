#include <stdio.h>
int main()
{
	int dno;
	scanf("%d", &dno);
	if(dno==1)
		printf("MONDAY");
	else if(dno==2)
		printf("TUESDAY");
	else if(dno==3)
		printf("WEDNESDAY");
	else if(dno==4)
		printf("THURSDAY");
	else if(dno==5)
		printf("FRIDAY");
	else if(dno==6)
		printf("SATURDAY");
	else if(dno==7)
		printf("SUNDAY");
	
}
